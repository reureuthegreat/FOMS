package com.Category;

/**
 * Represents categories for items such as burgers, drinks, side dishes, and set meals.
 */

public enum Category {

    /**
     * Represents the category for burgers.
     */
    BURGER,

    /**
     * Represents the category for drinks.
     */
    DRINK,

    /**
     * Represents the category for side dishes.
     */
    SIDEDISH,

    /**
     * Represents the category for set meals.
     */
    SETMEAL;
}
